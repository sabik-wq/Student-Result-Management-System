    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String id = jTextField2.getText();
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/usrm","root","");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student inner join result where student.id = '"+id+"' and result.id = '"+id+"'");
            if(rs.next()){
                setVisible(false);
                new studentHome(id).setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null, "ID is Incorreect");
            }

        }
        catch(HeadlessException | ClassNotFoundException | SQLException e){
            JOptionPane.showMessageDialog(null, "Connection Error");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

