    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String schoolname = (String)jComboBox1.getSelectedItem();
        String department = (String)jComboBox2.getSelectedItem();
        String id = jTextField1.getText();
        String name = jTextField2.getText();
        String gender = (String)jComboBox3.getSelectedItem();
        String father_name = jTextField4.getText();
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/usrm","root","");
            try (Statement st = con.createStatement()) {
                st.executeUpdate("insert into student (school,department,id,name,gender,fatherName) values ('"+schoolname+"','"+department+"','"+id+"','"+name+"','"+gender+"','"+father_name+"')");
                JOptionPane.showMessageDialog(null, "Successfully Updated");
                setVisible(false);
                new adminHome().setVisible(true);
            }
        }
        catch(HeadlessException | ClassNotFoundException | SQLException e){
            JOptionPane.showMessageDialog(null, "This ID already exists");
            setVisible(false);
            new adminHome().setVisible(true);
        }
    }//GEN-LAST:event_jButton6ActionPerformed

