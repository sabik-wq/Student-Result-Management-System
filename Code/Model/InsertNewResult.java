    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String id = jTextField1.getText();
        String phy = jTextField2.getText();
        String che = jTextField3.getText();
        String math = jTextField4.getText();
        String cse = jTextField5.getText();
        String cod = jTextField6.getText();
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/usrm","root","");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student where id = '"+id+"'");
            if(rs.next()){
                st.executeUpdate("insert into result (id,phy,che,math,cse,cod) values ('"+id+"','"+phy+"','"+che+"','"+math+"','"+cse+"','"+cod+"')");
                JOptionPane.showMessageDialog(null, "Successfully Updated");
                setVisible(false);
                new InsertNewResult().setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null, "ID Does not exist");
                setVisible(false);
                new InsertNewResult().setVisible(true);
            }
        }
        catch(HeadlessException | ClassNotFoundException | SQLException e){
            JOptionPane.showMessageDialog(null, "Connection Error");
        }
    }//GEN-LAST:event_jButton6ActionPerformed

